namespace Ombor.Contracts.Enums;

/// <summary>
/// Enumeration representing the employment status of an employee in the system..
/// </summary>
public enum EmployeeStatus
{
    /// <summary>
    /// Employee is currently active and working.
    /// </summary>
    Active,

    /// <summary>
    /// Employee is on leave or temporarily not working.
    /// </summary>
    Terminated,

    /// <summary>
    /// Employee is on vacation.
    /// </summary>
    OnVacation
}
